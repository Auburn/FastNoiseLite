	j1 = SIMPLEX4D[c][1] >= 3 ? 1 : 0;
	k1 = SIMPLEX4D[c][2] >= 3 ? 1 : 0;
	l1 = SIMPLEX4D[c][3] >= 3 ? 1 : 0;
