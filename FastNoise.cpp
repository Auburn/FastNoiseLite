float FastNoise::GetValCoordNoLUT(int seed, int x, int y, int z, int w)
{
	int n = X_PRIME * x;
	n ^= Y_PRIME * y;
	n ^= Z_PRIME * z;
	n ^= W_PRIME * w;
	n ^= seed;
	n &= 0x7fffffff;
	n = (n >> 13) ^ n;
	return 1.0f - (((n * (n * n * 60493 + 19990303) + 1376312589) & 0x7fffffff) / 1073741824.0f);
}

