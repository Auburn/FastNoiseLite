	float y0 = y - Y0;
	float z0 = z - Z0;
	float w0 = w - W0;

	int c = (x0 > y0) ? 32 : 0;
	c += (x0 > z0) ? 16 : 0;
	c += (y0 > z0) ? 8 : 0;
	c += (x0 > w0) ? 4 : 0;
	c += (y0 > w0) ? 2 : 0;
	c += (z0 > w0) ? 1 : 0;
	int i1, j1, k1, l1; 
	int i2, j2, k2, l2; 
	int i3, j3, k3, l3; 
	
