inline int FastNoise::CoordLUTIndex(int seed, int x, int y, int z, int w)
{
	int hash = seed;
	hash ^= x;
	hash *= X_PRIME;
	hash ^= y;
	hash *= Y_PRIME;
	hash ^= z;
	hash *= Z_PRIME;
	hash ^= w;
	hash *= W_PRIME;
	hash ^= hash >> 16;

	return hash & LUT_MASK;
}

